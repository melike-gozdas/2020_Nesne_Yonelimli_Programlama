package Ornek1;

public class Cocuk {
	public String ad;
	public int yas;
	public Cocuk(String ad, int yas) {
		this.ad = ad;
		this.yas = yas;
	}
	public String getAd() {
		return ad;
	}
	public void setAd(String ad) {
		this.ad = ad;
	}
	public int getYas() {
		return yas;
	}
	public void setYas(int yas) {
		this.yas = yas;
	}
	
	
	

}
